#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*s;

	i = 0;
	s = (unsigned char *)b;
	while (len > i)
	{
		s[i] = (unsigned char)c;
		i++;
	}
	return (s);
}
